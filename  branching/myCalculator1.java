
public class myCalculator1 {

	public static void main(String[] args) {
		int a=25;
		float b=0.5f;
		System.out.println("**********MY CALCULATOR**********");
		System.out.println("1. addition result is "+(a+b));
		System.out.println("2. subtraction result is "+(a-b));
		System.out.println("3. multiplication result is "+(a*b));
		System.out.println("4. division result is "+(a/b));
		System.out.println("5. modulus result is "+(a%b));
		System.out.println("6. squareroot of a is "+Math.sqrt(a));
		System.out.println("7. a to power b value is "+Math.pow(a, b));

	}

}
