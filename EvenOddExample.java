package edu.learningJava.loops.exercises;

public class EvenOddExample {

	public static void main(String[] args) {
		int i=0;
		while(i<=30){
			if(i%2==0){
				System.out.print(i+" " );
		i++;
			}}
			System.out.print(" are even");
			System.out.println();
			while(i<=30){
			if(i%2!=0){
			System.out.print(i+" ");
			i++;
			}
			System.out.println(" are odd");
					
			}
	

	}

}
